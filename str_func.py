def lift_up():
    """
    Функция принимает на вход строку и возвращает ее со всеми заглавными буквами
    """
    stroka = input()
    print(stroka.upper())

def lift_up_first():
    """
    Функция делает заглавными первые буквы каждого слова в строке
    """
    stroka = input()
    print(stroka.title())